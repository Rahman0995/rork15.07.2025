#!/bin/bash

# Скрипт для изменения прав доступа к app.json
echo "Изменяем права доступа к app.json..."

# Даем права 777 (чтение, запись, выполнение для всех)
chmod 777 app.json

# Проверяем результат
if [ $? -eq 0 ]; then
    echo "✅ Права успешно изменены!"
    echo "Текущие права app.json:"
    ls -la app.json
else
    echo "❌ Ошибка при изменении прав"
fi